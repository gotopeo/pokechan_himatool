/** 攻撃タイプ→防御タイプへの倍率を取得 */
export function getTypeMatchup(atk: PokemonType, def: PokemonType): number {
  return TYPE_CHART[TYPE_INDEX[atk]][TYPE_INDEX[def]]
}

/** 防御側が持つ2タイプへの攻撃タイプ倍率を計算 */
export function getDefensiveMatchup(atk: PokemonType, type1: PokemonType, type2?: PokemonType): number {
  const m1 = getTypeMatchup(atk, type1)
  const m2 = type2 ? getTypeMatchup(atk, type2) : 1
  return m1 * m2
}
